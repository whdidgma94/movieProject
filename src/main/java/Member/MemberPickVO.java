package Member;

public class MemberPickVO {
	private String memberId;
	private String movieLike;
	private String movieSeen;
	
	public String getMemberId() {
		return memberId;
	}
	public void setMemberId(String memberId) {
		this.memberId = memberId;
	}
	public String getMovieLike() {
		return movieLike;
	}
	public void setMovieLike(String movieLike) {
		this.movieLike = movieLike;
	}
	public String getMovieSeen() {
		return movieSeen;
	}
	public void setMovieSeen(String movieSeen) {
		this.movieSeen = movieSeen;
	}
	
}
